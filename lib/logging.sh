#!/usr/bin/env bash

# ---------------------------------------------------------
# Logging helpers (safe under set -euo pipefail)
# ---------------------------------------------------------

# Colors
CLR_RESET="\e[0m"
CLR_BOLD="\e[1m"
CLR_DIM="\e[2m"
CLR_OK="\e[32m"
CLR_WARN="\e[33m"
CLR_ERR="\e[31m"
CLR_INFO="\e[34m"
CLR_TITLE="\e[35m"

# ---------------------------------------------------------
# SAFE echo wrapper (never fails)
# ---------------------------------------------------------
log::print() {
    printf "%b\n" "$1" || true
}

# ---------------------------------------------------------
# Title block
# ---------------------------------------------------------
log::title() {
    log::print "${CLR_TITLE}${CLR_BOLD}$1${CLR_RESET}"
}

# ---------------------------------------------------------
# Section header
# ---------------------------------------------------------
log::section() {
    log::print ""
    log::print "▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒"
    log::print "  ${CLR_TITLE}${CLR_BOLD}$1${CLR_RESET}"
    log::print "▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒"
}

# ---------------------------------------------------------
# Step
# ---------------------------------------------------------
log::step() {
    log::print "${CLR_TITLE}${CLR_BOLD}[Step]${CLR_RESET} $1"
}

# ---------------------------------------------------------
# Substep
# ---------------------------------------------------------
log::substep() {
    log::print "    ${CLR_DIM}→${CLR_RESET} $1"
}

# ---------------------------------------------------------
# Info
# ---------------------------------------------------------
log::info() {
    log::print "${CLR_INFO}[INFO]${CLR_RESET} $1"
}

# ---------------------------------------------------------
# OK
# ---------------------------------------------------------
log::ok() {
    log::print "${CLR_OK}[OK]${CLR_RESET} $1"
}

# ---------------------------------------------------------
# Warning
# ---------------------------------------------------------
log::warn() {
    log::print "${CLR_WARN}[WARN]${CLR_RESET} $1"
}

# ---------------------------------------------------------
# Error (non‑fatal)
# ---------------------------------------------------------
log::error() {
    log::print "${CLR_ERR}[ERROR]${CLR_RESET} $1"
}

# ---------------------------------------------------------
# Fatal error (exits)
# ---------------------------------------------------------
log::die() {
    log::print "${CLR_ERR}[FATAL]${CLR_RESET} $1"
    exit 1
}

# ---------------------------------------------------------
# Success block
# ---------------------------------------------------------
log::success_block() {
    log::print ""
    log::print "▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒"
    log::print "│ ${CLR_OK}$1${CLR_RESET}"
    log::print "▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒▒"
    log::print ""
}
